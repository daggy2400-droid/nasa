// Helper function to create SVG icon
function createIcon(iconName, className = 'icon') {
    if (!ICONS[iconName]) {
        console.warn(`Icon '${iconName}' not found`);
        return `<svg class="${className}" viewBox="0 0 16 16"><circle cx="8" cy="8" r="6" fill="currentColor"/></svg>`;
    }
    
    return `<svg class="${className}" viewBox="0 0 16 16">${ICONS[iconName]}</svg>`;
}

// Function to replace all generic circle icons with proper icons
function replaceGenericIcons() {
    // Replace navigation icons
    const navItems = document.querySelectorAll('.nav-item a');
    navItems.forEach(item => {
        const href = item.getAttribute('href');
        let iconName = 'home';
        
        if (href.includes('/product')) iconName = 'product';
        else if (href.includes('/team')) iconName = 'team';
        else if (href.includes('/my')) iconName = 'my';
        else if (href.includes('/help')) iconName = 'help';
        
        const iconElement = item.querySelector('svg');
        if (iconElement && iconElement.innerHTML.includes('circle cx="8" cy="8" r="6"')) {
            iconElement.innerHTML = ICONS[iconName];
        }
    });
    
    // Replace card icons
    const cardIcons = document.querySelectorAll('.card-icon svg');
    cardIcons.forEach((icon, index) => {
        const card = icon.closest('.card');
        if (card) {
            const href = card.getAttribute('href');
            let iconName = 'gift';
            
            if (href.includes('/gift')) iconName = 'gift';
            else if (href.includes('/service')) iconName = 'service';
            else if (href.includes('/purchase')) iconName = 'purchase';
            else if (href.includes('/invite')) iconName = 'invite';
            
            if (icon.innerHTML.includes('circle cx="8" cy="8" r="6"')) {
                icon.innerHTML = ICONS[iconName];
            }
        }
    });
    
    // Replace header icons
    const headerIcons = document.querySelectorAll('.header svg, h1 svg, h2 svg, h3 svg');
    headerIcons.forEach(icon => {
        if (icon.innerHTML.includes('circle cx="8" cy="8" r="6"')) {
            const text = icon.parentElement.textContent.toLowerCase();
            let iconName = 'home';
            
            if (text.includes('investment') || text.includes('my')) iconName = 'my';
            else if (text.includes('product')) iconName = 'product';
            else if (text.includes('team')) iconName = 'team';
            else if (text.includes('help')) iconName = 'help';
            else if (text.includes('gift')) iconName = 'gift';
            else if (text.includes('service')) iconName = 'service';
            else if (text.includes('invite')) iconName = 'invite';
            else if (text.includes('wallet')) iconName = 'wallet';
            
            icon.innerHTML = ICONS[iconName];
        }
    });
}

// Auto-replace icons when DOM is loaded
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', replaceGenericIcons);
} else {
    replaceGenericIcons();
}

